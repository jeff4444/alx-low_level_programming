#include "3-3-3-calc.h"
/**
 * op_add - adds two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - find the difference of two ints
 * @a: first integer
 * @b: second integer
 *
 * Return: difference of a and b
 */
int op_sub(int a, int b)
{
        return (a - b);
}

/**
 * op_mul - multiply two ints
 * @a: first integer
 * @b: second integer
 *
 * Return: product of a and b
 */
int op_mul(int a, int b)
{
        return (a * b);
}

/**
 * op_div - divide two ints
 * @a: first integer
 * @b: second integer
 *
 * Return: quotient of a and b
 */
int op_div(int a, int b)
{
        return (a / b);
}

/**
 * op_mod - modulo of two int
 * @a: first integer
 * @b: second integer
 *
 * Return: a % b
 */
int op_mod(int a, int b)
{
       return (a % b);
}
